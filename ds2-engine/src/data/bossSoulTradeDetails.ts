export interface BossSoulWeaponDetails {
  type: "weapon";
  weaponClass: string;
  damage: { physical?: number; magic?: number; fire?: number; lightning?: number; dark?: number };
  damageMax?: { physical?: number; magic?: number; fire?: number; lightning?: number; dark?: number };
  scaling: { str?: string; dex?: string; int?: string; fth?: string };
  requirements: { str?: number; dex?: number; int?: number; fth?: number };
  weight: number;
  durability?: number;
  counterStrength?: number;
  upgradeMaterial: string;
  description?: string;
  specialEffect?: string;
}

export interface BossSoulShieldDetails {
  type: "shield";
  shieldClass: string;
  damage?: { physical?: number };
  stability: number;
  stabilityMax?: number;
  damageReduction: { physical: number; magic?: number; fire?: number; lightning?: number; dark?: number };
  weight: number;
  requirements?: { str?: number };
  upgradeMaterial: string;
  description?: string;
}

export interface BossSoulSpellDetails {
  type: "sorcery" | "miracle" | "pyromancy" | "hex";
  slots: number;
  uses: number;
  intReq?: number;
  fthReq?: number;
  description?: string;
  effect?: string;
}

